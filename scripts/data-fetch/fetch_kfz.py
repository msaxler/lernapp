def main():
    if not STAEDTE_JSON.exists():
        print(f"FEHLER: {STAEDTE_JSON} nicht gefunden!")
        sys.exit(1)

    with open(STAEDTE_JSON, encoding='utf-8') as f:
        staedte = json.load(f)
    print(f"[JSON] {len(staedte)} Städte geladen")

    treffer = 0
    kein_treffer = []

    for stadt in staedte:
        # Bereits vorhanden → überspringen
        if stadt.get('kfz'):
            treffer += 1
            continue

        ars  = str(stadt.get('ars', '')).zfill(12)
        ags5 = ars[:5]
        kfz  = AGS5_KFZ.get(ags5)

        if kfz:
            stadt['kfz'] = kfz
            treffer += 1
        else:
            kein_treffer.append((ags5, stadt.get('name', '?')))

    print(f"[OK]     {treffer}/{len(staedte)} Städte mit KFZ ({treffer/len(staedte)*100:.1f}%)")
    if kein_treffer:
        print(f"[OFFEN]  {len(kein_treffer)} ohne KFZ:")
        for ags5, name in kein_treffer:
            print(f"  AGS5={ags5}  {name}")

    with open(STAEDTE_JSON, 'w', encoding='utf-8') as f:
        json.dump(staedte, f, ensure_ascii=False, indent=2)
    print(f"[SAVED]  {STAEDTE_JSON}")


if __name__ == '__main__':
    main()
